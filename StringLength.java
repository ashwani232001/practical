package String;

public class StringLength {
    public static void main(String[] args) {
        String str = "Anudip Foundation";
        
         // One way to find string length.
        System.out.println("The string length of '"+str+"' is : "+str.length());
        
        // Another way to find string length.
        int len = str.length();
        System.out.println("The string length of '"+str+"' is : "+len);
    }
}
